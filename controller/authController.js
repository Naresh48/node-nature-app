// Signup:
// When a user signs up, their details (name, email, password) are validated
// and saved to the database. A JWT is generated and sent back to the client in a cookie,
// logging the user in immediately.

// Login:
// When a user logs in, their email and password are checked. If they are valid,
// a JWT is generated and sent back, allowing the user to access protected routes.

// Protect Route:
// Middleware ensures that only users with a valid JWT (token) can access certain routes.
// The token is checked, and if it is valid, the user is allowed to proceed.

// Forgot/Reset Password:
// If a user forgets their password, they can request a password reset by providing their email
//. A token is generated and emailed to them. They can then use this token to reset their password.
// The token expires after 10 minutes.

// Update Password:
// Logged-in users can update their password by providing their current password and setting a new one.
// After updating, a new JWT is generated to keep the user logged in with their new credentials.

const crypto = require('crypto');
const { promisify } = require('util');
const jwt = require('jsonwebtoken');
const User = require('./../models/userSchemaModel');
const catchAsync = require('./../utils/catchAsync');
const AppError = require('./../utils/appError');
const Email = require('./../utils/email');

////HELPER FUNCTIONS////////////////////////
//Creates a JWT (JSON Web Token) for authentication,
//which includes the user's ID and is signed with a secret key.
const signToken = (id) => {
  return jwt.sign({ id }, process.env.JWT_SECRET, {
    expiresIn: process.env.JWT_EXPIRES_IN,
  });
};

//This function creates the JWT, sets a cookie with the token, and sends the user data in the response.
//The token is stored in a cookie to facilitate user authentication.
//JWT stores in http cookie with expiration time.
const createSendToken = (user, statusCode, res) => {
  const token = signToken(user._id);
  const cookieOptions = {
    expires: new Date(
      Date.now() + process.env.JWT_COOKIE_EXPIRES_IN * 24 * 60 * 60 * 1000,
    ),
    httpOnly: true,
  };
  if (process.env.NODE_ENV === 'production') cookieOptions.secure = true;

  res.cookie('jwt', token, cookieOptions);

  // Remove password from output
  user.password = undefined;

  res.status(statusCode).json({
    status: 'success',
    token,
    data: {
      user,
    },
  });
};
